# ============================================================================
# Minimal, dependency-free YAML emitter (output is valid YAML; strings are
# JSON-quoted, which YAML accepts — guarantees correctness without PyYAML).
# ============================================================================
def _scalar(v):
    if isinstance(v, bool):
        return "true" if v else "false"
    if isinstance(v, (int, float)):
        return str(v)
    return json.dumps(str(v), ensure_ascii=False)


def to_yaml(obj, indent=0):
    pad = "  " * indent
    out = []
    if isinstance(obj, dict):
        for k, v in obj.items():
            if isinstance(v, (dict, list)) and v:
                out.append(f"{pad}{k}:")
                out.append(to_yaml(v, indent + 1))
            else:
                out.append(f"{pad}{k}: {_scalar(v)}")
    elif isinstance(obj, list):
        for item in obj:
            if isinstance(item, dict):
                inner = to_yaml(item, indent + 1).split("\n")
                out.append(f"{pad}- {inner[0].lstrip()}")
                out.extend(inner[1:])
            else:
                out.append(f"{pad}- {_scalar(item)}")
    return "\n".join(out)


# ============================================================================
# Live inventory scan (keeps the file list current)
# ============================================================================
SELF_OUTPUTS = {"MOTHERLODE.yaml", "MOTHERLODE.md", "llms.txt"}


def inventory():
    # Exclude this script's own generated outputs so the count is stable across runs.
    files = [p for p in ROOT.rglob("*") if p.is_file()
             and "__pycache__" not in p.parts and not p.name.startswith(".DS")
             and p.name not in SELF_OUTPUTS]
    by_dir = {}
    for p in files:
        rel = p.relative_to(ROOT)
        top = rel.parts[0] if len(rel.parts) > 1 else "(root)"
        by_dir[top] = by_dir.get(top, 0) + 1
    return {
        "total_files": len(files),
        "by_directory": dict(sorted(by_dir.items())),
        "generated": {
            "per_deal_pages": len(list(ROOT.glob("deal-*.html"))),
            "category_pages": len(list(ROOT.glob("cat-*.html"))),
            "history_snapshots": len(list((ROOT / "history").glob("deals-*.json"))) if (ROOT / "history").exists() else 0,
        },
    }



def render_md(bp):
    m = bp["meta"]
    L = [f"# 🜲 MOTHERLODE — {m['name']} blueprint", "",
         f"> {m['tagline']}  ", f"> **{m['type']}** · blueprint v{m['blueprint_version']}", "",
         "This is the master knowledge base for this site — written so a human **or another AI**",
         "can rebuild the whole system on a new site. The machine-readable twin is",
         "[`MOTHERLODE.yaml`](MOTHERLODE.yaml); an LLM index is [`llms.txt`](llms.txt).", "",
         "## Stack"]
    for k, v in bp["stack"].items():
        L.append(f"- **{k}**: {v}")
    L += ["", "## Architecture"]
    L += [f"- {a}" for a in bp["architecture"]]
    L += ["", "## Components"]
    for c in bp["components"]:
        L.append(f"### `{c['files']}`")
        L.append(f"_{c['kind']}_ — {c['purpose']}")
        if c.get("notes"):
            L.append(f"  ↳ {c['notes']}")
        L.append("")
    L += ["## Security posture"]
    for k, v in bp["security"].items():
        L.append(f"- **{k}**: {v}")
    L += ["", "## Data model"]
    for k, v in bp["data_model"].items():
        L.append(f"- **{k}**: {v}")
    L += ["", "## Reusable patterns"]
    L += [f"- {p}" for p in bp["patterns"]]
    L += ["", "## Key decisions"]
    L += [f"- **{d['choice']}** — {d['why']}" for d in bp["decisions"]]
    L += ["", "## Roadmap / future changes"]
    L += [f"- [{d['status']}] {d['item']}" for d in bp["roadmap"]]
    L += ["", "## Rebuild this for a NEW site"]
    L += bp["rebuild_for_new_site"]
    inv = bp["inventory"]
    L += ["", "## Live inventory (auto-scanned)",
          f"- **Total files:** {inv['total_files']}",
          f"- **Generated:** {inv['generated']['per_deal_pages']} per-deal pages, "
          f"{inv['generated']['category_pages']} category pages, "
          f"{inv['generated']['history_snapshots']} history snapshots",
          "- **By directory:** " + ", ".join(f"{k} ({v})" for k, v in inv["by_directory"].items()), ""]
    L += ["---", "_Regenerate with `python3 scripts/motherlode.py` (also runs in the daily build). "
          "Edit the `BLUEPRINT` in `scripts/motherlode.py` to document new features or roadmap items._"]
    return "\n".join(L) + "\n"



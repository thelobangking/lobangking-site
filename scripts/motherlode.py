def main():
    bp = dict(BLUEPRINT)
    bp["inventory"] = inventory()
    (ROOT / "MOTHERLODE.yaml").write_text(
        "# MOTHERLODE — structured blueprint (AI-ingestible). Generated by scripts/motherlode.py.\n"
        + to_yaml(bp) + "\n", encoding="utf-8")
    (ROOT / "MOTHERLODE.md").write_text(render_md(bp), encoding="utf-8")
    (ROOT / "llms.txt").write_text(render_llms_txt(bp), encoding="utf-8")
    print(f"Motherlode updated — {bp['inventory']['total_files']} files documented "
          f"→ MOTHERLODE.yaml, MOTHERLODE.md, llms.txt")


